#include "libft.h"

char	*ft_strdup(const char *s)
{
	size_t	len;
	char	*dst;

	len = ft_strlen(s);
	dst = malloc(len);
	if (dst)
		ft_strlcpy(dst, s, len + 1);
	return (dst);
}
