#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	size_t	len;
	char	*dst;

	len = ft_strlen(s);
	dst = malloc(len + 1);
	if (dst)
	{
		dst[len] = 0;
		while (len--)
			dst[len] = (*f)(len, dst[len]);
	}
	return (dst);
}
